<?php

namespace CHU\EtudeBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Question_Type
 *
 * @ORM\Table(name="Question_Type")
 * @ORM\Entity(repositoryClass="CHU\EtudeBundle\Repository\Question_TypeRepository")
 */
class Question_Type
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="Label", type="string", length=255, unique=true)
     */
    private $label;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set label
     *
     * @param string $label
     *
     * @return Question_Type
     */
    public function setLabel($label)
    {
        $this->label = $label;

        return $this;
    }

    /**
     * Get label
     *
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }
}
